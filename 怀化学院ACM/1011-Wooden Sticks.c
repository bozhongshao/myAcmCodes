#include"stdio.h"
int main()
{
	int t,z1,z2,lw[10000],i,j,s1=0,s2=0,n,k=0,a;
	scanf("%d",&t);
	for(k=0;k<t;k++)
	{
		scanf("%d",&n);
		for(i=0;i<2*n;i++)
		{
			scanf("%d",&lw[i]);
		}
		for(i=2;i<2*n-1;i=i+2)
		{
			z1=lw[i];
			z2=lw[i+1];
			j=i-2;
			while(j>=0&&lw[j]>=z1)
			{
				if(lw[j]=z1)
				{
					if(lw[j+1]>lw[i+1])
					{
						a=lw[j+1];lw[j+1]=lw[i+1];lw[i+1]=a;
					}
				}
				else
				{
					lw[j+2]=lw[j];
				    lw[j+3]=lw[j+1];
				    j=j-2;
				}
			}
			lw[j+2]=z1;
			lw[j+3]=z2;
		}

        for(i=1;i<2*n;i=i+2)
		{
			if(lw[i]>lw[i+2])
				s1=s1+1;
		}
		for(i=3;i<2*n;i=i+2)
		{
			z1=lw[i];
			z2=lw[i-1];
			j=i-2;
			while(j>=1&&lw[j]>=z1)
			{
				if(lw[j]=z1)
				{
					if(lw[j-1]>lw[i-1])
					{
						a=lw[j-1];lw[j-1]=lw[i-1];lw[i-1]=a;
					}
				}
				else
				{
					lw[j+2]=lw[j];
				    lw[j+1]=lw[j-1];
				    j=j-2;
				}
			}
			lw[j+2]=z1;
			lw[j+1]=z2;
		}
		for(i=0;i<2*n;i=i+2)
		{
			if(lw[i]>lw[i+2])
				s2=s2+1;
		}
		if(s1>s2)
			printf("%d\n",s2);
		else
			printf("%d\n",s1);
		s1=0;s2=0;
		for(i=0;i<2*n;i++)
		{
			lw[i]=0;
		}
	}
	return 0;
}